def has_followup_question(text: str) -> bool:
    """
    กัน false positive กรณีข้อความมีคำลา/ขอบคุณ แต่ยังมีคำถามจริงต่อ
    เช่น:
        "ขอบคุณนะคะ แล้วเมื่อไหร่จะมีโปรโมชั่นใหม่"
        "ผมเข้าใจแล้วว่าเคลมยังไง แต่ถ้าเกิดเหตุตอนกลางคืนล่ะ"

    หมายเหตุ:
        ใช้เฉพาะเป็น guard ของ FAREWELL
        ถ้าข้อความสั้นมาก เช่น "ขอบคุณครับ" จะไม่ถือว่าเป็นคำถามต่อ
    """
    if len(text) < 16:
        return False

    return match_any(QUESTION_CONTINUATION_PATTERNS, text)


def classify_smalltalk(question: str) -> Optional[str]:
    """
    เช็คแบบ keyword / regex ก่อนเรียก LLM หรือ embedding

    คืนค่า:
        - "greeting"  = คำทักทาย / เริ่มบทสนทนา
        - "farewell"  = คำลา / ขอบคุณ / จบบทสนทนา
        - None        = ไม่ตรง pattern ใดเลย หรือเสี่ยงเป็นคำถามจริง
    """
    text = normalize_text(question)

    if not text:
        return None

    # เช็ค greeting ก่อน เพราะคำว่า "สบายดี" มีคำว่า "บาย" อยู่ข้างใน
    if match_any(GREETING_PATTERNS, text):
        return "greeting"

    # ถ้ามีคำลา แต่ในประโยคยังมีคำถามจริง ให้ปล่อยเข้า pipeline หลัก
    if match_any(FAREWELL_PATTERNS, text):
        if has_followup_question(text):
            return None
        return "farewell"

    return None


def get_smalltalk_response(question: str) -> Optional[str]:
    """
    คืน response ตาม intent
    ถ้าไม่ใช่ smalltalk ให้คืน None
    """
    intent = classify_smalltalk(question)

    if intent == "greeting":
        return GREETING_RESPONSE

    if intent == "farewell":
        return FAREWELL_RESPONSE

    return None



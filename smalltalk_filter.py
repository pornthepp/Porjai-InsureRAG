# คำลงท้าย / คำสุภาพ / คำเสริม ที่เอาออกก่อน classify
REMOVE_TOKENS = [
    "ครับ", "ค่ะ", "คะ", "คับ", "ค้าบ", "ฮะ",
    "นะ", "น้า", "จ้า", "จ๊ะ", "จ้ะ", "ค่า", "คร้า",
]


def normalize_text(text: str) -> str:
    """
    ปรับข้อความให้ตรวจจับง่ายขึ้น
    เช่น ตัดคำสุภาพ ลด space ซ้ำ และแปลงอังกฤษเป็นตัวเล็ก
    """
    if not text:
        return ""

    text = text.strip().lower()

    for token in REMOVE_TOKENS:
        text = text.replace(token, "")

    # รวมช่องว่างซ้ำให้เหลือช่องว่างเดียว
    text = re.sub(r"\s+", " ", text).strip()

    return text


def match_any(patterns: list[str], text: str) -> bool:
    """
    ตรวจว่า text ตรงกับ pattern ใด pattern หนึ่งหรือไม่
    """
    return any(re.search(pattern, text, flags=re.IGNORECASE) for pattern in patterns)


